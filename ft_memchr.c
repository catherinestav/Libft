#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*d;

	i = 0;
	d = (unsigned char *)s;
	while (i < n)
	{
		if (*d == (unsigned char)c)
			return (d);
		d++;
		i++;
	}
	return (NULL);
}
